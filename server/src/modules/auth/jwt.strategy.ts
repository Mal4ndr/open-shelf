/**
 * Цей файл є однією з частин модуля аутентифікації разом з guards.
 * Стратегія вважається "ядром перевірки" (витягує токен, декодує, валідує).
 *
 * Guards - є "охоронцями" на рівні контролерів. Вони активують стратегію при кожному запиті,
 * перевіряють результат і або пропускають (додають req.user), або повертають 401.
 *
 * Разом вони забезпечують повний цикл:
 * запит > Guard активує Strategy > перевірка > req.user або помилка.
 *
 * Без стратегії Guards не знають, як перевіряти (немає алгоритму). Без Guards стратегія не
 * викликається (немає "тригера" на запитах). Без модуля Auth, NestJS не інжектує стратегію
 * (немає реєстрації). Це як ланцюжок: модуль реєструє, Guards запускають, стратегія виконує.
 * Всі три обов'язкові складвові для функціональної аутентифікації.
 */

import { Injectable } from '@nestjs/common';

/**
 * Загалом passport рахується набором різних іструментів «strategies» для аутентифікації.
 * Одним з таких іструментів (стратегією) є JWT (існують й інші: (local, oauth2, …).
 * А PassportStrategy є інтерпретацією passport в NestJS.
 */
import { PassportStrategy } from '@nestjs/passport';

/**
 * Стратегія (JwtStrategy) є модулем/класом, який бере дані з запиту (jwtFromRequest),
 * перевіряє підпис (secret), опрацьовує decoded payload через validate.
 * ExtractJwt є утилітою для витягування токена з запиту.
 */
import { ExtractJwt, Strategy } from 'passport-jwt';

/**
 * @Injectable() - декоратор, що робить з класа сервіс, який має здатність бути інжектованим
 * в інші модулі, сервіси та контролери. JwtStrategy є конкретним класом для jwt-перевірки
 * і наслідує базову стратегію Passport.
 */
@Injectable()
export class JwtStrategy extends PassportStrategy(Strategy) {
  constructor() {
    /**
     * super є ключовим словом для роботи з наслідуванням, воно дозволяє викликати методи
     * або конструктор батьківського класу з дочірнього. super у конструкторі ініціалізує
     * батьківський клас перед тим як дочірній додає свою логіку. Без super(), при умові, що
     * батьківський клас має конструктор (як у цьому випадку), код не скомпілюється або
     * викинеться помилка: "Вимагає ініціалізації батька". Якщо батьківський конструктор
     * порожній, super() можна опустити, але в NestJS/Passport це завжди потрібно.
     *
     * super({ ... }) викликає конструктор PassportStrategy(Strategy) (батьківський клас
     * з passport-jwt). Він передає конфігурацію (jwtFromRequest, secretOrKey) у внутрішню
     * логіку Passport, щоб стратегія знала, як витягувати токен, перевіряти підпис і декодувати
     * payload.
     */
    super({
      /**
       * super() передає конфігураційний об'єкт у passport-jwt.Strategy. Стратегія в свою чергу
       * приймає опції як об'єкт, щоб налаштувати поведінку.
       *
       * jwtFromRequest: ExtractJwt.fromAuthHeaderAsBearerToken(): Читає Bearer token — витягує
       * токен з заголовка Authorization: Bearer <token>. Це стандартний спосіб для Bearer токенів.
       *
       * ignoreExpiration: false: Не ігнорує термін дії токена (якщо токен прострочений, стратегія
       * відхилить його).
       *
       * secretOrKey: Перевіряє secret — секретний ключ для перевірки підпису JWT.
       * Якщо підпис невірний, токен відхилиться.
       */
      jwtFromRequest: ExtractJwt.fromAuthHeaderAsBearerToken(),
      ignoreExpiration: false,
      secretOrKey: 'super-secret', // значення слід замінити на зміну з оточення
    });
  }

  /**
   * validate() - метод, який Passport викликає після успішного декодування токена. Він
   * дозволяє перевірити payload і "перетворити" його на користувача (або відхилити, якщо
   *  щось не так). Результат повертається як request.user у контролерах.
   */
  async validate(payload: any) {
    return {
      userId: payload.sub,
      email: payload.email,
    };
  }
}
